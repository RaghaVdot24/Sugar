package com.example.demo22;

public class CategoryTuple {
    private int id,count;
    public CategoryTuple(int x,int y) {
        this.id = x;
        this.count = y;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    
}